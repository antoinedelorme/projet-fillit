#include "libft.h"

void	ft_putnbr(int n)
{
	long big;

	big = n;
	if (big < 0)
	{
		ft_putchar('-');
		big = big * -1;
	}
	if (big >= 10)
	{
		ft_putnbr((big / 10));
		ft_putnbr((big % 10));
	}
	else
	{
		ft_putchar((big + '0'));
	}
}
