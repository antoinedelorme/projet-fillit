#include "libft.h"

void	ft_putchar_fd(char c, int fd)
{
	unsigned char chr[2];

	if (c >= 0)
	{
		write(fd, &c, 1);
		return ;
	}
	chr[0] = (c >= -64) ? 195 : 194;
	chr[1] = (c >= -64) ? c + 192 : c + 256;
	write(fd, chr, 2);
}
